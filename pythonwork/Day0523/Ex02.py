"""
    파이썬의 연산자에 대해서 알아 봅시다
    +, -, *, /, %, //, **
"""
num1=int(input("숫자 1 입력:"))
num2=int(input("숫자 2 입력:"))
print("더하기:",(num1+num2))
print("빼기:",(num1-num2))
print("곱하기:",(num1*num2))
print("나누기:",(num1/num2))
print("나머지:",(num1%num2))
print("나누기한 후 몫만:",(num1//num2))
print("지수승:",(num1**num2))
